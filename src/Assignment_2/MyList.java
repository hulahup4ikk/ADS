package Assignment_2; // Принадлежность пакету Assignment_2

// Интерфейс MyList<T> описывает набор операций, которые должны поддерживаться списками
public interface MyList<T> {

    // Добавляет элемент в конец списка
    void add(T item);

    // Устанавливает значение item по заданному индексу
    void set(int index, T item);

    // Вставляет элемент item на заданную позицию index (сдвигает элементы вправо)
    void add(int index, T item);

    // Вставляет элемент в начало списка
    void addFirst(T item);

    // Вставляет элемент в конец списка (аналогично add())
    void addLast(T item);

    // Возвращает элемент по заданному индексу
    T get(int index);

    // Возвращает первый элемент списка
    T getFirst();

    // Возвращает последний элемент списка
    T getLast();

    // Удаляет элемент по заданному индексу
    void remove(int index);

    // Удаляет первый элемент списка
    void removeFirst();

    // Удаляет последний элемент списка
    void removeLast();

    // Сортирует элементы списка (требуется реализация Comparable у элементов)
    void sort();

    // Возвращает индекс первого вхождения заданного объекта (или -1, если не найден)
    int indexof(Object object);

    // Возвращает индекс последнего вхождения заданного объекта (или -1, если не найден)
    int lastindexof(Object object);

    // Проверяет, существует ли объект в списке
    boolean exists(Object object);

    // Преобразует список в массив объектов
    Object[] toArray();

    // Удаляет все элементы из списка
    void clear();

    // Возвращает текущее количество элементов в списке
    int size();
}
